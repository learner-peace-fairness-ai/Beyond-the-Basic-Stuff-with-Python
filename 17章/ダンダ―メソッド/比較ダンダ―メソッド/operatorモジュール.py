import operator

print(operator.eq(42, 42))  # "eq"は"EQual"の略で、 42 == 42 という意味

print(operator.ne("cat", "dog"))  # "ne"は"Not Equal"の略で、 "cat" != "dog" という意味

print(operator.gt(10, 20))  # "gt"は"Greater Than"の略で、 10 > 20 という意味

print(operator.ge(10, 10))  # "ge"は"Greater than or Equal"の略で、 10 >= 10 という意味

print(operator.lt(10, 20))  # "lt"は"Less Than"の略で、 10 < 20 という意味

print(operator.le(10, 20))  # "le"は"Less than or Equal"の略で、 10 <= 20 という意味
